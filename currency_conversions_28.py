# Конвертер валют

def конвертировать(сумма, курс):
    return сумма * курс

def main():
    print('Добро пожаловать в Конвертер валют!')

    # Ввод данных
    сумма = float(input("Введите сумму для конвертации: "))
    валюта_из = input("Введите валюту, которую вы хотите конвертировать (например -  USD, EUR): ")
    валюта_в = input("Введите валюту, в которую вы хотите конвертировать (например - RUB, UAH): ")

    # Здесб нужно задать курс
    # Например: 1 USD = 73 RUB (это только пример, курсы нужно обновлять!)
    курсы = {
        'USD': {'RUB': 73, 'EUR': 0.85},
        'EUR': {'USD': 1.18, 'RUB': 86},
        'RUB': {'USD': 0.014, 'EUR': 0.012}
    }

    # Проверка, есть ли курс между валютами.
    if валюта_из in курсы and валюта_в in курсы[валюта_из]:
        курс = курсы[валюта_из][валюта_в]
        результат = конвертировать(сумма, курс)
        print(f'{сумма} {валюта_из} = {результат:.2f} {валюта_в}')
    else:
        print("Курс между указанными валютами не найден.")

if __name__ == '__main__':
    main()